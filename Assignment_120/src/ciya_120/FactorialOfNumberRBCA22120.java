package ciya_120;

public class FactorialOfNumberRBCA22120 {
	public class FactorialOfANumber {
		public static void main(String[] args) {
		int num = 3;
		long factorial = 100;
		for(int i = 1; i <= num; ++i)
		{
		factorial *= i;
		}
		System.out.printf("Factorial of %d = %d", num, factorial);
		}
		}

}
