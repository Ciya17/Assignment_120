package ciya_120;

public class PersonRBCA22120 {
	    private String name;
	    private int age;

	    // Constructor
	    public PersonRBCA22120(String name, int age) {
	        this.name = name;
	        this.age = age;
	    }

	    // Getter methods
	    public String getName() {
	        return name;
	    }

	    public int getAge() {
	        return age;
	    }

	    // Setter methods
	    public void setName(String name) {
	        this.name = name;
	    }

	    public void setAge(int age) {
	        this.age = age;
	    }
	}

